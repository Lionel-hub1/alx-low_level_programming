#include "3-calc.h"

/**
 * op_add - This function is for adding two numbers
 * @a: The first number
 * @b: The second number
 * Return: Their sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - This function is for subtracting two numbers
 * @a: The first number
 * @b: The second number
 * Return: Their difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - This function is for multiplying two numbers
 * @a: The first number
 * @b: The second number
 * Return: Their product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - This function is for dividing two numbers
 * @a: The first number
 * @b: The second number
 * Return: Their quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - This function is for finding remainder of two numbers
 * @a: The first number
 * @b: The second number
 * Return: Their remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
